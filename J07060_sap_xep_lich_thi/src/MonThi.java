public class MonThi {
    private String id,ten,hinhThucThi;

    public MonThi(String id, String ten, String hinhThucThi) {
        this.id = id;
        this.ten = ten;
        this.hinhThucThi = hinhThucThi;
    }

    public String getId() {
        return id;
    }

    public String getTen() {
        return ten;
    }

    public String getHinhThucThi() {
        return hinhThucThi;
    }
}
