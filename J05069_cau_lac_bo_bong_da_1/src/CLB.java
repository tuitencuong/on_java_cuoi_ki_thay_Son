public class CLB {
    private String id,ten;
    private int giaVe;

    public CLB(String id, String ten, int giaVe) {
        this.id = id;
        this.ten = ten;
        this.giaVe = giaVe;
    }

    public String getId() {
        return id;
    }

    public String getTen() {
        return ten;
    }

    public int getGiaVe() {
        return giaVe;
    }
}
